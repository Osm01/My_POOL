int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	y;

	i = 0;
	y = 0;
	while ((s1[i] != '\0' || s2[y] != '\0') && s1[i] == s2[y])
	{
		i ++;
		y ++;
	}
	return (s1[i] - s2[y]);
}
